import { CharMap, TransformType } from '../types';

// Helper to generate map from two strings
const createMap = (source: string, target: string): CharMap => {
  const map: CharMap = {};
  const sourceChars = source.split('');
  // Use spread for target to handle surrogate pairs correctly if any, though most math alphanumeric are surrogate pairs
  const targetChars = Array.from(target);
  
  sourceChars.forEach((char, index) => {
    if (targetChars[index]) {
      map[char] = targetChars[index];
    }
  });
  return map;
};

